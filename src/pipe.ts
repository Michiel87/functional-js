/**
 * @description (((a, b, …, n) → o), (o → p), …, (x → y), (y → z)) → ((a, b, …, n) → z
 */

export function pipe (...fns: any[]) {
  return (x: any) => fns.reduce((v, f) => f(v), x)
}
